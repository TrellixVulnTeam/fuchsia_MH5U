typedef struct {
  uint8_t repeat;
  uint8_t intensity;
} rle_entry_t;

void logo_load(efi_graphics_output_blt_pixel* pixels) {
  const rle_entry_t* entry = (const rle_entry_t*)logo_rle;
  const size_t num_entries = sizeof(logo_rle) / sizeof(*entry);
  const rle_entry_t* entry_end = entry + num_entries;

  for (; entry != entry_end; ++entry) {
    efi_graphics_output_blt_pixel pixel = {.Blue = scale_color(logo_b, entry->intensity),
                                           .Green = scale_color(logo_g, entry->intensity),
                                           .Red = scale_color(logo_r, entry->intensity)};

    // Copy this pixel |repeat + 1| times.
    for (int i = 0; i <= entry->repeat; ++i) {
      *(pixels++) = pixel;
    }
  }
}
